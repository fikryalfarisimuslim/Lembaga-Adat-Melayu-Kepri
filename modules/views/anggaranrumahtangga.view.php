SETIA AMANAH<br>
<br>
Pasal 3<br>
<br></b></center>
<ol type="1">
<li>Setia Amanah Adat berfungsi sebagai Payung, Panji, Penasehat dan Pembimbing utama masyarakat melayu Kepulauan Riau untuk Daerah bersangkutan.</li>
<li>Setia Amanah Adat di tingkat Provinsi Kepulauan Riau adalah Gubernur Kepulauan Riau yang merupakan gelar kehormatan tertinggi di tingkat Provinsi, sebutan kehormatan disebut Datok.</li>
<li>Setia Amanah Adat di tingkat Kabupaten/ Kota adalah Bupati/ Walikota yang merupakan gelar kehormatan tertinggi di tingkat Kabupaten/ Kota bersangkutan dengan sebutan Datok.</li>
