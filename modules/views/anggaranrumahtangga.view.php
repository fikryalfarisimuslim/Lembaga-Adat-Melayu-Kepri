<li>Pengurus Lembaga Adat Melayu Kepulauan Riau Kabupaten/ Kota.</li>
<li>Utusan Pengurus Lembaga Adat Melayu Kepulauan Riau Kecamatan.</li>
<li>Pemuka Adat Melayu, Ulama dan Cendikiawan yang dipandang perlu menurut Pengurus Lembaga Adat Melayu Kepulauan Riau Kabupaten/ Kota.</li>
<li>Utusan Pengurus Organisasi Kerukunan Melayu Kepulauan Riau tingkat Kabupaten/ Kota.</li>
<li>Musyawarah Daerah Lembaga Adat Melayu Kepulauan Riau Kabupaten/ Kota mempunyai wewenang :</li>
<li>Menilai Laporn Pertangggung Jawaban Pengurus.</li>
<li>Menetapkan Garis-garis Besar Program Kerja 5 (lima) tahun.</li>
<li>Memilih dan Menetapkan Pengurus Lembaga Adat Melayu Kepulauan Riau Kabupaten/ Kota.</li>
<li>Membuat dan Menetapkan Keputusan lainnya.</li>
</ol>
