														KEPENGURUSAN<br>
<br>
PEMBENTUKAN PENGURUS<br>
<br>
Pasal 4<br>
<br></b></center>
Pembentukan Pengurus Lembaga Adat Melayu Kepulauan Riau dilakukan :<br>

<ol type="1">
<li>Pengurus Provinsi oleh Musyawah Besar.</li>
<li>Pengurus Kabupaten/ Kota oleh Musyawarah Daerah.</li>
<li>Pengurus Kecamatan oleh Musyarah Kecamatan.</li>
<li>Pengurus Kelurahan/ Desa oleh Musyawarah Kecamatan/ Desa.</li>
<li>Pemilihan Pengurus Lembaga Adat Melayu Kepulauan Riau secara Langsung/ Formatur.</li>
<li>Pengurus Lembaga Adat Melayu Kepulauan Riau berfungsi melaksanakan Keputusan-keputusan Musyawarah, Rapat Kerja dan Keputusan Lain Lembaga Adat Melayu Kepulauan Riau.</li>
<li>Pengurus Lembaga Adat Melayu Kepulauan Riau melapotkan pelaksanaan tugasnya pada Musywarah Lembaga Adat Melayu Kepulauan Riau.</li>
<li>Pengurus Lembaga Adat Melayu Kepulauan Riau berwenang mengukuhkan Pengurus Lembaga Adat Melayu Kepulauan Riau setingkat dibawahnya.</li>
</ol><br><center><b>SUSUNAN PENGURUS<br>
<br>
Pasal 5<br>
<br></b></center>
Susunan Dewan Pengurus Lembaga Adat Melayu Kepulauan Riau disemua tingkat terdiri atas :<br>

<ol type="1">
<li>Ketua Umum dan Ketua – Ketua;</li>
<li>Sekretaris Umum dan Sekretaris – Sekretaris;</li>
<li>Bendahara dan Wakil – Wakil Bendahara;</li>
<li>Ketua – Ketua Bidang beserta Anggota.</li>
<li>Dewan Pengurus terdiri atas Pengurus Pleno dan Pengurus Harian.</li>
<li>Pengurus Pleno adalah seluruh Pengurus Lembaga Adat Melayu Kepulauan Riau.</li>
<li>Pengurus Harian terdiri atas Ketua Umum, Ketua – Ketua, Sekretaris Umum, Sekretaris – Sekretaris, Bendahara dan Wakil – Wakil Bendahara.</li>

</ol>
<br><center><b>SYARAT – SYARAT PENGURUS<br>
<br>
Pasal 6<br>
<br></b></center>
Syarat – syarat menjadi Pengurus Lembaga Adat Melayu Kepulauan Riau :<br>

<ol type="1">
<li>Aktif menjadi anggota.</li>
<li>Memiliki prestasi, dedikasi, desiplin, loyalitas dan tidak tercela.</li>
<li>Setiap pengurus dilarang merangkap jabatan dalam Kepengurusan yang bersifat Vertikal.</li>
<li>Syarat – syarat menjadi Ketua Umum Pengurus Lembaga Adat Melayu Kepulauan Riau :</li>
<li>Memahami Anggaran Dasar dan Anggran Rumah Tangga Lembaga Adat Melayu Kepulauan Riau;</li>
<li>Berakhlaqul Karimah;</li>
<li>Memiliki Ilmu tentang Adat Istiadat dan Budaya Melayu;</li>
<li>Berdomisili di Ibukota Provinsi Kepulauan Riau.</li>
<li>Pernah menjadi Pengurus Lembaga Adat Melayu Kepulauan Riau;</li>
<li>Aktif terus menerus menjadi Pengurus Lembaga Adat Melayu Kepulauan Riau sekurang – kurangnya satu periode.</li>
<li>Memiliki Prestasi, dedikasi, desiplin, loyalitas dan tidak tercela;</li>
<li>Bersedia meluangkan waktu dan sanggup bekerjasama secara kolektif.</li>
<li>Tidak merangkap jabatan sebagai pimpinan puncak partai politik.</li>

</ol>
<br>
<center><b>DEWAN KEHORMATAN<br>
<br>
Pasal 7<br>
<br></b></center>

<ol type="1">
<li>Dewan Kehormatan Lembaga Adat Melayu Kepulauan Riau berfungsi memberikan pertimbangan pemikiran dan pemecahan masalah yang dihadapi Pengurus Lembaga Adat Melayu Kepulauan Riau.</li>
<li>Susunan Dewan Kehormatan berjumlah 17 orang terdiri dari :</li>
<li>Seorang Ketua dan Anggota</li>
<li>Dewan Kehormatan terdiri dari Pemuka Masyarakat, Tokoh Adat, Tokoh Agama yang ada di Ibukota Provinsi maupun di Daerah Kabupaten/ Kota lain.</li>

</ol>
<br>
<center><b>PENGURUS HARIAN<br>
<br>
Pasal 8<br>
<br></b></center>


<ol type="1">
<li>Pengurus Harian Lembaga Adat Melayu Kepulauan Riau berfungsi melaksanakan tugas Pengurus Lembaga Adat Melayu Kepulauan Riau sehari-hari pada tingkatnya dan bertanggung jawab kepada Pengurus Lembaga Adat Melayu Kepulauan Riau.</li>
<li>Pengurus Harian Lembaga Adat Melayu Kepulauan Riau mengadakan pembagian tugas sebagai berikut :</li>
<li>Ketua Umum memimpin pelaksanaan dan fungsi Pengurus Lembaga Adat Melayu Kepulauan Riau secara keseluruhan.</li>
<li>Ketua-ketua membantu Ketua Umum dan mengkoordinir Bidang-bidang/ Seksi-seksi sesuai dengan pembidangannya.</li>
<li>Sekretaris Umum membantu Ketua Umum dan para Ketua serta memimpin Administrasi Kesekretariatan Lembaga Adat Melayu Kepulauan Riau.</li>
<li>Sekretaris-sekretaris membantu Sekretaris Umum.</li>
<li>Bendahara membantu Ketua Umum dan para Ketua serta memimpin Administrasi Keuangan.</li>
<li>Wakil Bendahara membantu Bendahara.</li>
<li>Ketua Umum Harian terpilih tidak diperkenankan memiliki jabatan rangkap.</li>
</ol>
<br>
<center><b>BIDANG-BIDANG<br>
<br>
Pasal 9<br>
<br></b></center>
Pengurus Lembaga Adat Melayu Kepulauan Riau dilengkapi dengan Bidang-Bidang yang terdiri dari :<br>

<ol type="1">
<li>Organisasi / Kaderisasi.</li>
<li>Penelitian/ Pengkajian/ Penulisan Adat dan Budaya Melayu Kepulauan Riau.</li>
<li>Ekonomi dan Koperasi.</li>
<li>Penegakan dan Bantuan Hukum.</li>
<li>Pemberdayaan Perempuan.</li>
<li>Kepemudaan dan Pengembangan SDM.</li>
<li>Lingkungan dan Pengembangan SDA.</li>
<li>Hubungan Adat dan Budaya Melayu.</li>
<li>Sosial dan Budaya.</li>
<li>Hubungan Masyarakat dan Pemerintah.</li>
<li>Bidang-bidang lainnya yang dianggap perlu.</li>

</ol>


