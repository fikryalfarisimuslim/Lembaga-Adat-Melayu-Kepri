														<center><b>MUSYAWARAH DAN RAPAT-RAPAT<br>
<br>
Pasal 13<br>
<br></b></center>
Musyawarah Lembaga Adat Melayu Kepulauan Riau terdiri dari :
<ol type="1">

<li>Musyawarah Besar (MUBES) Lembaga Adat Melayu Kepulauan Riau tingkat Provinsi Kepulauan Riau diadakan sekali dalam 5 (Lima) tahun.</li>
<li>Musyawarah Daerah (MUSDA) Lembaga Adat Melayu Kepulauan Riau tingkat Kabupaten/ Kota diadakan sekali dalam 5 (lima) tahun.</li>
<li>Musyawarah Kecamatan (MUSCAM), Musyawarah Kelurahan/ Desa (MUSLUR/ MUSDES) Lembaga Adat Melayu Kepulauan Riau tingkat Kecamatan, Kelurahan/ Desa diadakan sekali dalam 5 (lima) tahun.</li>
<li>Musywarah Luar Biasa Lembaga Adat Melayu Kepulauan Riau dapat diadakan apabila Musyawarah Besar atau Musyawarah Daerah atau Musyawarah Kecamatan, Kelurahan/ Desa tidak dapat dilaksanakan karena sesuatu hal atau karena kepengurusan tidak dapat melaksanakan tugas sesuai denagan AD/ ART.</li>
<li>Rapat Kerja diadakan sekali dalam 1 (satu) tahun untuk menevaluasi kegiatan dan menyusun kegiatan tahunan.</li>
<li>Rapat Pengurus dapat diadakan sewaktu-waktu yang diperlukan.</li>
<li>Pada Dasarnya semua Rapat/ Persidangan bersifat terbuka kecuali jika dianggap perlu dapat dinyatakan tertutup untuk umum.</li>
<li>Tata Tertib Musyawarah dan Rapat-rapat diatur tersendiri.</li>
</ol>
<br>
<center><b>MUSYAWARAH BESAR<br>
<br>
Pasal 14<br>
<br></b></center>
Peserta Musyawarah Besar Lembaga Adat Melayu Kepulauan Riau adalah :
<ol type="1">

<li>Pengurus Lembaga Adat Melayu Kepulauan Riau Provinsi Kepulauan Riau.</li>
<li>Utusan Pengurus Lembaga Adat Melayu Kepulauan Riau Kabupaten/ Kota.</li>
<li>Pemuka Adat Melayu, Ulama dan Cendikiawan yang dipandang perlu menurut Pengurus Lembaga Adat Melayu Kepulauan Riau.</li>
<li>Utusan Pengurus Kerukunan Melayu tingkat Provinsi Kepulauan Riau.</li>
<li>Musyawarah Besar Lembaga Adat Melayu Kepulauan Riau mempunyai wewenang :</li>
<li>Menilai Laporan Pertanggung Jawaban Pengurus.</li>
<li>Menetapkan atau mengubah Anggaran Dasar dan Anggaran Rumah Tangga.</li>
<li>Menetapkan Garis-garis Besar Program Kerja 5 (lima) tahun.</li>
<li>Memilih dan Menetapkan Pengurus Lembaga Adat Melayu Kepulauan Riau.</li>
<li>Membuat dan Menetapkan Keputusan lainnya.</li>
</ol>
<br>
<center><b>MUSYAWARAH DAERAH<br>
<br>
Pasal 15<br>
<br></b></center>
