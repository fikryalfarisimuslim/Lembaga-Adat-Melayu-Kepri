<p>
<b>Bismillahirrahmanirrahim</b><br>
<br>
Sejarah telah membuktikan bahwa keberhasilan suatu bangsa dalam pembangunannya sangat ditentukan oleh budaya itu sendiri. Rumusan ini sudah menjadi kesepakatan dikalangan pakar sosiologi dunia, bahkan Perserikatan Bangsa-Bangsa dalam pemberian bantuannya menekankan bahwa pembangunan suatu bangsa haruslah berdasarkan atas budaya bangsa itu sendiri.<br><br>

Kekayaan Adat Resam Melayu yang bersendi syara’ dan nilai-nilai luhur budaya dan norma-norma social masyarakatnya, menjadikan adat memegang peranan penting serta kedudukan terhormat dalam kehidupan pribadi, bermasyarakat, berbangsa dan bernegara. Adat bukan saja membentuk kepribadian yang beradat akan tetapi lebih dari itu mampu mewujudkan keamanan dan ketertiban, kedamaian dan kesejahteraan dalam kehidupan sehari-hari.<br><br>

“Tak ada kusut yang tak terselesaikan, tak ada keruh yang tak terjernihkan” demikian pepatah adat. Hal ini terlihat dari perilaku masyarakat adat dalam menyelesaikan suatu permasalahan, dimana “Berat sama dipikul, Ringan sama dijinjing, Sakit jenguk menjenguk, Senang jelang menjelang” sehingga tercipta iklim dan semangat gotong royong, saing membantu senasib sepenanggungan, se aib semalu, yang pada gilirannya mengokohkan persatuan dan kesatuan serta menumbuhkan rasa memiliki dan bertanggung jawab yang tinggi atas semua yang dimiliki masyarakat adat.<br><br>

Melalui adat dikembangkan pula sifat-sifat terpuji, yakni insane-insan yang bertaqwa, berkepribadian, berwawasan luas, kreatif, inovatif dan memiliki etos kerja yang tinggi, profesional serta bertanggung jawab. Insan-insan inilah dalam ungkapan digambarkan “Hidup beradat membawa tuah, kecil menjadi tuan rumah, Besar menjadi tuan negeri, Cerdik menjadi penyambung lidah, Berani menjadi pelapis, Pandai tempatnya bertanya, Alimnya tempat bertuah, Mudanya menjadi contoh, Tuanya menjadi teladan, Budinya menjadi ikutan, Bahasanya menjadi pegangan”.<br><br>

Sadar akan kedudukan, tugas dan kewajiban dan bertanggung jawab kami sebagai warga Negara Indonesia, wajib berperan aktif dalam pembangunan bangsa ini dalam meningkatkan kesejahteraan rakyat melalui Pembangunan Ekonomi yang efektif dan memperkokoh persatuan dan kesatuan bangsa demi terwujudnya ketahanan nasional.<br><br>

Kami atas nama masyarakat Melayu Provinsi Kepulauan Riau, didorong oleh keinginan luhur dengan ini menyatakan, berhimpun dalam suatu wadah Organisasi “Lembaga Adat Melayu Kepulauan Riau ( LAM KEPRI )” dengan Anggaran Dasar dan Anggaran Rumah Tangga.</p>
